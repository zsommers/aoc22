func Test_execute(t *testing.T) {
	tests := []struct {
		instruction string
		want        []int
	}{
		{"noop", []int{1, 1}},
		{"addx 3", []int{1, 1, 4}},
		{"addx -3", []int{1, 1, -2}},
	}
	for _, tt := range tests {
		t.Run(tt.instruction, func(t *testing.T) {
			assert.Equal(t, tt.want, execute(tt.instruction, []int{1}))
		})
	}
}
